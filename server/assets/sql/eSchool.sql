DROP DATABASE IF EXISTS eschool;

CREATE DATABASE eschool;

USE eschool;

CREATE TABLE addresses(
    address_id INT AUTO_INCREMENT PRIMARY KEY,
    division VARCHAR(50) NOT NULL,
    district VARCHAR(50) NOT NULL,
    upazilla VARCHAR(50) NOT NULL,
    postal_code INT NOT NULL
);

CREATE TABLE students(
    student_id INT AUTO_INCREMENT PRIMARY KEY,
    first_name VARCHAR(100) NOT NULL,
    last_name VARCHAR(100) NOT NULL,
    gender VARCHAR(10) NOT NULL,
    date_of_birth DATETIME NOT NULL,
    image_src VARCHAR(255),
    phone VARCHAR(15),
    email VARCHAR(255),
    address_id INT NOT NULL,
    FOREIGN KEY(address_id) REFERENCES addresses(address_id)
);

CREATE TABLE parents(
    parent_id INT AUTO_INCREMENT PRIMARY KEY,
    first_name VARCHAR(100) NOT NULL,
    last_name VARCHAR(100) NOT NULL,
    nid VARCHAR(100) NOT NULL,
    phone VARCHAR(15) NOT NULL,
    email VARCHAR(255),
    image_src VARCHAR(255) NOT NULL,
    address_id INT NOT NULL,
    FOREIGN KEY(address_id) REFERENCES addresses(address_id)
);

CREATE TABLE student_parent(
    student_id INT NOT NULL,
    parent_id INT NOT NULL,
    relation VARCHAR(10),
    UNIQUE(student_id, parent_id),
    FOREIGN KEY(student_id) REFERENCES students(student_id),
    FOREIGN KEY(parent_id) REFERENCES parents(parent_id)
);

CREATE TABLE subjects(
    subject_id INT AUTO_INCREMENT PRIMARY KEY,
    sub_name VARCHAR(255)
);

CREATE TABLE teaches(
    teacher_id INT AUTO_INCREMENT PRIMARY KEY,
    first_name VARCHAR(100) NOT NULL,
    last_name VARCHAR(100) NOT NULL,
    position VARCHAR(100) NOT NULL,
    salary VARCHAR(10) NOT NULL,
    image_src VARCHAR(255) NOT NULL,
    phone VARCHAR(15) NOT NULL,
    email VARCHAR(255),
    facebook VARCHAR(255),
    linkedin VARCHAR(255),
    address_id INT NOT NULL,
    FOREIGN KEY(address_id) REFERENCES addresses(address_id)
);

CREATE TABLE teacher_subjects(
    teacher_id INT NOT NULL,
    subject_id INT NOT NULL,
    FOREIGN KEY(teacher_id) REFERENCES teaches(teacher_id),
    FOREIGN KEY(subject_id) REFERENCES subjects(subject_id)
);

CREATE TABLE staffs(
    staff_id INT AUTO_INCREMENT PRIMARY KEY,
    first_name VARCHAR(100) NOT NULL,
    last_name VARCHAR(100) NOT NULL,
    position VARCHAR(100) NOT NULL,
    salary VARCHAR(10) NOT NULL,
    image_src VARCHAR(255) NOT NULL,
    phone VARCHAR(15) NOT NULL,
    email VARCHAR(255),
    facebook VARCHAR(255),
    linkedin VARCHAR(255),
    address_id INT NOT NULL,
    FOREIGN KEY(address_id) REFERENCES addresses(address_id) ON DELETE CASCADE
);

CREATE TABLE principals(
    principal_id INT AUTO_INCREMENT PRIMARY KEY,
    teacher_id INT NOT NULL,
    principal_text VARCHAR(255) NOT NULL,
    start_from DATETIME NOT NULL,
    end_to DATETIME NOT NULL,
    FOREIGN KEY(teacher_id) REFERENCES teaches(teacher_id)
);

CREATE TABLE school(
    eiin VARCHAR(255) NOT NULL,
    school_name VARCHAR(255) NOT NULL,
    principal_id INT NOT NULL,
    address_id INT NOT NULL,
    phone VARCHAR(15) NOT NULL,
    email VARCHAR(255) NOT NULL,
    established_at DATETIME NOT NULL,
    FOREIGN KEY(principal_id) REFERENCES principals(principal_id),
    FOREIGN KEY(address_id) REFERENCES addresses(address_id)
);

CREATE TABLE academics(
    academic_id INT AUTO_INCREMENT PRIMARY KEY,
    class_name VARCHAR(20) NOT NULL,
    room_number INT,
    class_teacher_id INT NOT NULL,
    start_from DATE NOT NULL,
    end_to DATE,
    FOREIGN KEY(class_teacher_id) REFERENCES teaches(teacher_id)
);

CREATE TABLE class_subjects(
    academic_id INT NOT NULL,
    subject_id INT NOT NULL,
    UNIQUE(academic_id, subject_id),
    FOREIGN KEY(academic_id) REFERENCES academics(academic_id),
    FOREIGN KEY(subject_id) REFERENCES subjects(subject_id)
);

CREATE TABLE student_status(
    student_id INT NOT NULL,
    academic_id INT NOT NULL,
    FOREIGN KEY(student_id) REFERENCES students(student_id),
    FOREIGN KEY(academic_id) REFERENCES academics(academic_id),
    UNIQUE(student_id, academic_id)
);

CREATE TABLE ex_type(
    type_id INT AUTO_INCREMENT PRIMARY KEY,
    type_name VARCHAR(25)
);

CREATE TABLE exams(
    exam_id INT PRIMARY KEY AUTO_INCREMENT,
    type_id INT NOT NULL,
    exam_held DATE NOT NULL,
    FOREIGN KEY(type_id) REFERENCES ex_type(type_id)
);

CREATE TABLE marks(
    mark_id INT AUTO_INCREMENT PRIMARY KEY,
    student_id INT NOT NULL,
    subject_id INT NOT NULL,
    exam_id INT NOT NULL,
    cq DECIMAL(5, 2),
    mcq DECIMAL(5, 2),
    attendance INT,
    homework INT,
    FOREIGN KEY(student_id) REFERENCES students(student_id),
    FOREIGN KEY(subject_id) REFERENCES subjects(subject_id),
    FOREIGN KEY(exam_id) REFERENCES exams(exam_id)
);

